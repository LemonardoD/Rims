const possibleTires = (tires: string): string[] => {
	if (tires && tires.length % 9 === 0) {
		if (tires.length === 9) {
			return [tires];
		}
		if (tires.length > 9) {
			return tires.match(/.{9}/g) || [];
		}
	}
	return [];
};

export const rimConfigSearch = (tireInfo: string) => {
	const tireSizes = possibleTires(tireInfo);
	return tireSizes.map(tire => {
		const searchDiameter = tire.slice(-2) as Diameters;
		let width = "";
		for (const [key, value] of Object.entries(rimsConfigs[searchDiameter])) {
			if (value.includes(tire)) {
				width = key;
				break;
			}
		}
		return { diameter: searchDiameter, width };
	});
};

